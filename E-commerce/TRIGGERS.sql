
SHOW TRIGGERS LIKE 'CUSTOMERS';

	-- Prevent Negative Product Price
    
-- BEFORE INSERT
DELIMITER $$
CREATE TRIGGER PREVENT_NEGATIVE_PRICING_INSERT
BEFORE INSERT ON PRODUCTS
FOR EACH ROW				
BEGIN
		IF NEW.PRICE <= 0 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Price cannot be less than or equal to 0';
	END IF;
END $$
DELIMITER ;

-- BEFORE UPDATE
DELIMITER $$
CREATE TRIGGER PREVENT_NEGATIVE_PRICING_UPDATE
BEFORE UPDATE ON PRODUCTS
FOR EACH ROW				
BEGIN
		IF NEW.PRICE <= 0 THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Price cannot be less than or equal to 0';
	END IF;
END $$
DELIMITER ;

